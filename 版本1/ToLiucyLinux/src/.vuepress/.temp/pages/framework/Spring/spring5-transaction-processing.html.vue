<div class="language-markdown line-numbers-mode" data-ext="md"><pre v-pre class="language-markdown"><code>事务是保证业务操作完整性的一种数据库机制。

事务的4特点： A C I D
<span class="token list punctuation">1.</span> A(Atomicity) 	原子性 
<span class="token list punctuation">2.</span> C(Consistency) 	一致性
<span class="token list punctuation">3.</span> I(Isolation)		隔离性
<span class="token list punctuation">4.</span> D(durability) 	持久性
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><ol>
<li><strong>原子性</strong>（<code v-pre>Atomicity</code>）：事务是最小的执行单位，不允许分割。事务的原子性确保动作要么全部完成，要么完全不起作用；</li>
<li><strong>一致性</strong>（<code v-pre>Consistency</code>）：执行事务前后，数据保持一致，例如转账业务中，无论事务是否成功，转账者和收款人的总额应该是不变的；</li>
<li><strong>隔离性</strong>（<code v-pre>Isolation</code>）：并发访问数据库时，一个用户的事务不被其他事务所干扰，各并发事务之间数据库是独立的；</li>
<li><strong>持久性</strong>（<code v-pre>Durability</code>）：一个事务被提交之后。它对数据库中数据的改变是持久的，即使数据库发生故障也不应该对其有任何影响。</li>
</ol>
<p>🌈：<strong>只有保证了事务的持久性、原子性、隔离性之后，一致性才能得到保障。也就是说 A、I、D 是手段，C 是目的！</strong></p>
