<p>概念：他描述了事务解决嵌套问题的特征</p>
<p>什么叫做事务的嵌套：他指的是一个大的事务中，包含了若干个小的事务</p>
<p>问题：大事务中融入了很多小的事务，他们彼此影响，最终就会导致外部大的事务，丧失了事务的原子性</p>
</blockquote>
</li>
</ul>
