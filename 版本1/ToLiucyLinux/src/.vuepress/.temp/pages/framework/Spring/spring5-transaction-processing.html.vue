<p><strong>事务是逻辑上的一组操作，要么都执行，要么都不执行。</strong></p>
<p>相信大家应该都能背上面这句话了，下面我结合我们日常的真实开发来谈一谈。</p>
<p>我们系统的每个业务方法可能包括了多个原子性的数据库操作，比如下面的 <code v-pre>save()</code> 方法中就有两个原子性的数据库操作。这些原子性的数据库操作是有依赖的，它们要么都执行，要不就都不执行。</p>
