<template><div><h2 id="一、top-–-实时显示系统运行状态" tabindex="-1"><a class="header-anchor" href="#一、top-–-实时显示系统运行状态"><span>一、top – 实时显示系统运行状态</span></a></h2>
<blockquote>
<p>  top命令的功能是用于实时显示系统运行状态，包含处理器、内存、服务、进程等重要资源信息。运维工程师们常常会把top命令比作是“加强版的Windows任务管理器”，因为除了能看到常规的服务进程信息以外，还能够对处理器和内存的负载情况一目了然，实时感知系统全局的运行状态，非常适合作为接手服务器后执行的第一条命令。<br><br>
  动态查看进程信息，并且可以查看到系统负载，内存，CPU等使用情况。</p>
</blockquote>
