<p><strong><font color=red>1.procs</font></strong></p>
<font color=eoqne>r </font> 表示等待执行的任务数，当一段时间内这个数值一直超过CPU数说明当前系统出现CPU瓶颈；<font color=eoqne>b</font>   表示等待IO的进程数量。<br><br><p><strong><font color=red>2.memory</font></strong></p>
<font color=eoqne>swpd  </font>  虚拟内存已使用的大小，如果大于0，表示你的机器物理内存不足了，如果不是程序内存泄露的原因，那么你该升级内存了或者把耗内存的任务迁移到其他机器；<font color=eoqne>free </font> 空闲的物理内存的大小；<font color=eoqne>buff </font> Linux/Unix系统是用来存储，目录里面有什么内容，权限等的缓存；<font color=eoqne>cache  </font> 直接用来记忆我们打开的文件,给文件做缓冲。<br><br><p><strong><font color=red>3.swap</font></strong></p>
<font color=eoqne>si </font> 每秒从磁盘读入虚拟内存的大小，如果这个值大于0，表示物理内存不够用或者内存泄露了，要查找耗内存进程解决掉；<font color=eoqne>so </font>   每秒虚拟内存写入磁盘的大小，如果这个值大于0，同上。<br><br><p><strong><font color=red>4.io</font></strong></p>
<font color=eoqne>bi </font> 块设备每秒接收的块数量，这里的块设备是指系统上所有的磁盘和其他块设备，默认块大小是1024byte;<font color=eoqne>bo </font> 块设备每秒发送的块数量，例如我们读取文件，bo就要大于0。bi和bo一般都要接近0，不然就是IO过于频繁，需要调整。<br><br><p><strong><font color=red>5.system</font></strong></p>
<font color=eoqne>in </font>  每秒中断数，包括时钟中断；<font color=eoqne>cs </font>  表示每秒上下文切换;<p>这两个值越大，会看到由内核消耗的cpu时间会越多。<br><br><br>
<strong><font color=red>6.cpu</font></strong></p>
<font color=eoqne>us </font> 用户CPU时间；<font color=eoqne>sy </font> 系统CPU时间，如果太高，表示系统调用时间长，例如是IO操作频繁；<font color=eoqne>id </font> 空闲 CPU时间，一般来说，id + us + sy = 100,一般我认为id是空闲CPU使用率，us是用户CPU使用率，sy是系统CPU使用率；<font color=eoqne>wa </font> 等待IO CPU时间；<font color=eoqne>st </font> 等待IO CPU时间。</blockquote>
<h2 id="四、dstat-–-全能系统信息统计工具" tabindex="-1"><a class="header-anchor" href="#四、dstat-–-全能系统信息统计工具"><span>四、dstat – 全能系统信息统计工具</span></a></h2>
<blockquote>
<p>  dstat命令是一个用来替换vmstat、iostat、netstat、nfsstat和ifstat这些命令的工具，是一个全能系统信息统计工具。与sysstat相比，dstat拥有一个彩色的界面，在手动观察性能状况时，数据比较显眼容易观察；而且dstat支持即时刷新，譬如输入dstat 3即每三秒收集一次，但最新的数据都会每秒刷新显示。和sysstat相同的是，dstat也可以收集指定的性能资源，譬如dstat -c即显示CPU的使用情况。</p>
</blockquote>
